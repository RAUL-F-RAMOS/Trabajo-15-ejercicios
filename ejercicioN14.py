##Sentencia Switch
numero=int(input("digite un numero entre 1 a 5: "))
if numero==1:
    print("Es el número 1")
elif numero == 2:
    print("Es el número 2")
elif numero == 3:
    print("Es el número 3")
elif numero == 4:
    print("Es el número 4")
elif numero == 5:
    print("Es el número 5")
else:
    print("No está en el rango del 1 al 5")
